package src.Basic_03_16Dec;

public class lab044 {
    public static void main(String[] args) {

        // Decrement Operator

       int b =10;
        System.out.println(--b );
        System.out.println(b++ );
        System.out.println( ++b);
        System.out.println("----------");

        System.out.println(--b + b++ + ++b); // 9 + 9 + 11
        System.out.println(b); // 11

        // A -- b ,  EXP -- 9 , b -- 9
        // B  b++ , exp 9 , b - 10
       // c ++b , exp 11 , b - 11





    }
}
